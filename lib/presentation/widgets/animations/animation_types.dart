/// Different types of animations for loading indicators and other animated elements
enum AnimationType {
  /// Pulsing animation that scales up and down
  pulse,
  
  /// Rotation animation that spins continuously
  rotate,
  
  /// Bouncing animation that moves up and down
  bounce,
  
  /// Sliding animation that moves horizontally
  slide,
  
  /// Fading animation that changes opacity
  fade
}
